         </div>
         <div class="case-sectwo">
            <h4>So, what exactly is industry 4.0?</h4>
            <p>Industry 4.0 is referred to the concept when there is significant transformation in the way goods are produced, or when there is digitization of manufacturing.The transition of goods being produced through automated technology is called Industry 4.0. The version 4.0 represents the fourth revolution that has occurred in manufacturing. The first three being mechanization through water and steam power, mass production & assembly lines using electricity and adoption of computers & automation. Industry 4.0 is an enhanced concept of Industry 3.0 and uses smart and autonomous systems fuelled by Data, Computer Vision and Machine Learning, all falling under AI automation.</p>
         </div>
         <div class="case-sectwo">
            <h4>How is AI automation helping the automotive industry?</h4>
            <p>The automotive industry is widespread in its offerings and the entire lifecycle covers manufacturing, insurance, dealers & service centers, used car dealers and car rentals. AI automation caters to each aspect of the automotive industry by offering bespoke solutions that help derive the maximum mileage.</p>
         </div>
         
       
         <div class="case-sectwo">
            <h3>Here’s how AI automation helps increase speed and efficiency, while minimizing investment and reducing cost and labour dependency:</h3>
            <h4>Manufacturing:</h4>
            <p>It covers the manufacturing lifecycle and detects irregularities on automotive frames by use of Computer Vision and Defelectometry technology. Right from the inline process to under body and end-of-line inspection, the technology covers every aspect of the quality check without any human intervention, and does it faster, sharper and better.</p>
            <h4>Insurance:</h4>
            <p>Covering the two important aspects of automotive insurance, AI automation provides assessment and inspection for break-ins as well as accident claims. And helps in timely renewal of policies and spot settlements.</p>
            <h4>Dealers & Service Centers:</h4>
            <p>The technology, through machine-vision cameras ensures Pre-Delivery-Inspection of a vehicle that involves capturing images and certification, prior to affixing the PDI sticker, making the job easier for dealers.</p>
            <p>In the case of Service Centers, with AI automation, instant quotes are generated after an automated check of the vehicle is completed. By enabling detailed visual audit trails before servicing, it also helps in case of disputes and for future reference.</p>
            <h4>Used Car Dealers:</h4>
            <p>An automated health check of used cars via an inspection assures that every vehicle goes through a rigorous quality check. This puts the dealer in a reliable position and instills trust in customers when it come second-hand purchase.</p>
            <h4>Fleet Owners / Car Rentals:</h4>
            <p>Quality checks using drive-through rigs during check in/out of fleet are conducted through AI automation. This helps save time and labour on physical inspection of each vehicle and guarantees accurate checks and instant reconciliation.</p>
            <p>The wheels of the automotive industry are turning fast and AI automation is keeping pace, by offering solutions that are reliable, agile and cost-effective.</p>
